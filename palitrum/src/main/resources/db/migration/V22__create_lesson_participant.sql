-- ============================================================
COMMENT ON TABLE lesson_participant IS 'Журнал посещаемости и оценок – связь урока со студентом, статус посещения, оценка.';
COMMENT ON COLUMN lesson_participant.id IS 'Уникальный идентификатор записи.';
COMMENT ON COLUMN lesson_participant.lesson_id IS 'ID урока (ссылка на lesson).';
COMMENT ON COLUMN lesson_participant.user_id IS 'ID студента (ссылка на users).';
COMMENT ON COLUMN lesson_participant.enrollment_id IS 'ID зачисления студента на программу (опционально, ссылка на student_program).';
COMMENT ON COLUMN lesson_participant.attendance_status IS 'Статус посещаемости: PRESENT (присутствовал), ABSENT (отсутствовал), LATE (опоздал), EXCUSED (по уважительной причине).';
